const firstName = 'John';
const lastName = "Doe's";

const fullName = `${firstName} ${lastName}`

console.log(fullName); // Output: John Doe's

const age = 30;
const greeting = `Hello, my name is ${fullName} and I am ${age} years old.`;

console.log(greeting); // Output: Hello, my name is John Doe's, and I am 30 years old.

